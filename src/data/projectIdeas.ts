export function getIdeaById(id: string): ProjectIdea | undefined {
  return PROJECT_IDEAS.find((p) => p.id === id);
}

export function ideasByCategory(cat: ProjectCategory): ProjectIdea[] {
  return PROJECT_IDEAS.filter((p) => p.category === cat);
}

export function ideasRequiring(toolTypeId: string): ProjectIdea[] {
  return PROJECT_IDEAS.filter((p) => p.toolTypeIds.includes(toolTypeId));
}
